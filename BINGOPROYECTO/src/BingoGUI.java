    private void btnSacarBolaActionPerformed(java.awt.event.ActionEvent evt) {
        int numero = historial.sacarNumero(); // Nmero extrado
        // System.out.println("Nmero extrado: " + numero);
        historial.imprimirHistorial();

        // Crear el nombre del JLabel dinmicamente usando el nmero
        String labelName = "label" + numero;

        // Buscar el JLabel por su nombre y cambiar el color solo de la casilla que tiene el nmero
        try {
            // Usamos la reflexin para obtener el JLabel por su nombre
            javax.swing.JLabel label = (javax.swing.JLabel) this.getClass().getDeclaredField(labelName).get(this);

            // Cambiar el color del JLabel correspondiente al nmero extrado
            label.setForeground(Color.GREEN); // Cambiar el color a verde
        } catch (NoSuchFieldException | IllegalAccessException e) {
            e.printStackTrace();
        }

        // Cambiar el color de label76
        label76.setForeground(Color.GREEN);
        label76.setText(String.valueOf(numero));

        cambiarColorCasillas(numero);
        tablero.marcarNumero(numero);
        tablero.marcarNumero(500);

        // MODIFICAR PARA OTROS PATRONES

        boolean patronCompleto = patron.verificarPatron(tablero);

        // Mostrar resultado en consola
        if (patronCompleto) {
            System.out.println("Patrn de lnea horizontal completado!");
            btnSacarBola.setEnabled(false);
            JOptionPane.showMessageDialog(null,"BINGOOOO!!");
        } else {
            System.out.println("El patrn de lnea horizontal no est completo.");
        }

    }


