    private void cambiarColorCasillas(int numero) {
        // Recorremos las filas y columnas del tablero
        for (int i = 0; i < 5; i++) {
            for (int j = 0; j < 5; j++) {
                // Verificamos si el nmero de la casilla es igual al nmero extrado
                if (tablero.getTablero()[i][j].getNumero() == numero) {
                    // Ahora determinamos qu label cambiar basado en la columna y fila
                    String labelName = "";

                    // Cambiamos el nombre del label dependiendo de la columna (B, I, N, G, O)
                    switch (j) {
                        case 0: // Columna B
                            labelName = "labelCartaB" + (i + 1);
                            break;
                        case 1: // Columna I
                            labelName = "labelCartaI" + (i + 1);
                            break;
                        case 2: // Columna N
                            labelName = "labelCartaN" + (i + 1);
                            break;
                        case 3: // Columna G
                            labelName = "labelCartaG" + (i + 1);
                            break;
                        case 4: // Columna O
                            labelName = "labelCartaO" + (i + 1);
                            break;
                    }

                    // Usamos reflexin para cambiar el color del JLabel correspondiente
                    try {
                          // Nombre de los labels: label1, label2, ..., label75
                        javax.swing.JLabel label = (javax.swing.JLabel) this.getClass().getDeclaredField(labelName).get(this);
                        label.setForeground(Color.GREEN);  // Cambiar color a gris
                    } catch (NoSuchFieldException | IllegalAccessException e) {
                        e.printStackTrace();
                    }

                    // Salir del ciclo una vez que encontramos y cambiamos el color del JLabel
                    return;
                }
            }
        }
    }

